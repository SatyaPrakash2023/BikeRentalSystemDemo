package com.java.bms;

public class Payment {
	private int paymentID;
	private int rentalID;
	private String userName;
	private double amount;
	private String method;
	private Status status;
	public int getPaymentID() {
		return paymentID;
	}
	public void setPaymentID(int paymentID) {
		this.paymentID = paymentID;
	}
	public int getRentalID() {
		return rentalID;
	}
	public void setRentalID(int rentalID) {
		this.rentalID = rentalID;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public double getAmount() {
		return amount;
	}
	public void setAmount(double amount) {
		this.amount = amount;
	}
	public String getMethod() {
		return method;
	}
	public void setMethod(String method) {
		this.method = method;
	}
	public Status getStatus() {
		return status;
	}
	public void setStatus(Status status) {
		this.status = status;
	}
	public Payment(int paymentID, int rentalID, String userName, double amount, String method, Status status) {
		super();
		this.paymentID = paymentID;
		this.rentalID = rentalID;
		this.userName = userName;
		this.amount = amount;
		this.method = method;
		this.status = status;
	}
	public Payment() {
		super();
		// TODO Auto-generated constructor stub
	}
	@Override
	public String toString() {
		return "Payment [paymentID=" + paymentID + ", rentalID=" + rentalID + ", userName=" + userName + ", amount="
				+ amount + ", method=" + method + ", status=" + status + "]";
	}
	
	
	
}
